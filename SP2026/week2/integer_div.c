#include<stdio.h>

int main(){

    int num1 = 7;
    int num2 = 2;

    int div = 7/2;
    int rem = 7%2;
    
    printf("num1: %d divided by num2: %d = %d\n", num1, num2, div);
    printf("Remainder of num1: %d divided by num2: %d = %d\n", num1, num2, rem);

    return 0;

}











